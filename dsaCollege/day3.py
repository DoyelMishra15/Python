# range(x,y,z)
# x is starting, y ending, z is step(inc/decrease)

#y is excluded, i.e will consider till y-1 value

for i in range(1,10):
    print(i)

for i in range(1,10,1):
    print(i)

for i in range(1,11,3):
    print(i)

for i in range(1,10):
    print(i, end=" ")

for i in range(10,0,-2):
    print(i)

